let currentPhoto = 0;

let imagesData = [data1, data2, data3, data4, data5, data6, data7, data8, data9, data10, data11, data12, data13, data14, data15];

$('#photo').attr('src', imagesData[currentPhoto.photo]);


let loadPhoto = (photoNumber) => {
    $('#photo').attr('src', imagesData[photoNumber].photo);
}

loadPhoto(currentPhoto);

$('#arrowRight').click(() => {
    if (currentPhoto < 14) {
        currentPhoto++;
        loadPhoto(currentPhoto);
    } else {
        currentPhoto = 0;
        loadPhoto(currentPhoto);
    }
})

$('#arrowLeft').click(() => {
    if (currentPhoto > 0) {
        currentPhoto--;
        loadPhoto(currentPhoto);
    } else {
        currentPhoto = 14;
        loadPhoto(currentPhoto);
    }
})

imagesData.forEach((item, index) => {
    $('.thumbnails').append(`<img data-index="${index}" src="${item.photo}">`);
    $('.thumbnails img').click((event) => {
        let indexClicked = $(event.target).attr('data-index');
        let numberIndex = parseInt(indexClicked);
        console.log(numberIndex);
        currentPhoto = numberIndex;
        loadPhoto(currentPhoto);
    });
});
